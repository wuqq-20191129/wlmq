    private MakeCardQueryVo getQueryVo() {
        MakeCardQueryVo queryVo = new MakeCardQueryVo();
        queryVo.setMakeCardTimeBegin(DateHelper.str10yyyy_MM_ddToStr8yyyyMMdd(makeCardTimeBeginCb.getSelectedItem().toString()));
        queryVo.setMakeCardTimeEnd(DateHelper.str10yyyy_MM_ddToStr8yyyyMMdd(makeCardTimeEndCb.getSelectedItem().toString()));
        
        
        return queryVo;
        
    }

