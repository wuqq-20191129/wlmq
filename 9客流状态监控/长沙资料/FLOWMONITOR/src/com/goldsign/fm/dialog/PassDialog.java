    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        String passOrg = this.jPasswordFieldOrg.getText().trim();
       
        String passNew = this.jPasswordFieldNew.getText().trim();
        String passConfirm = this.jPasswordFieldConfirm.getText().trim();
        if(!this.checkInput(passNew,passConfirm))
             return;
         PassDao dao = new PassDao();
         PassResult ret ;
        try {
            ret =dao.setPass(passOrg, passNew);
            if(ret.isIsSuccess())
            {
                this.dispose();
                PubUtil.prompt(this,ret.getMsg());
                return;
            }
            else{
                PubUtil.prompt(this,ret.getMsg());
            }
        } catch (Exception ex) {
            Logger.getLogger(PassDialog.class.getName()).log(Level.SEVERE, null, ex);
        }




    }//GEN-LAST:event_jButtonOKActionPerformed

