    private boolean checkInput( String passNew ,String passConfirm ){

        
        if(passNew.length()==0){
            PubUtil.prompt(this,"新密码不能为空");
            PubUtil.focus(this.jPasswordFieldNew);
            return false;
        }
        if(passNew.length()!=6){
            PubUtil.prompt(this,"新密码应为6位");

             PubUtil.focus(this.jPasswordFieldNew);
            return false;
        }
        if(passConfirm.length()==0){
            PubUtil.prompt(this,"确认密码不能为空");
             PubUtil.focus(this.jPasswordFieldConfirm);
            
            return false;
        }
        if(passConfirm.length()!=6){
            PubUtil.prompt(this,"确认密码应为6位");
            PubUtil.focus(this.jPasswordFieldConfirm);
            return false;
        }
        if(!passNew.equals(passConfirm)){
            PubUtil.prompt(this,"新密码与确认密码不一致");
            PubUtil.focus(this.jPasswordFieldConfirm);
            return false;
        }
        return true;
    }
