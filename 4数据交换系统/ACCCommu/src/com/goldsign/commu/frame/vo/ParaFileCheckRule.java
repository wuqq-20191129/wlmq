package com.goldsign.commu.frame.vo;

/**
 * 
 * @author hejj
 */
public class ParaFileCheckRule {

	private String parmTypeId;
	private String operator;
	private int size;

	/**
	 * @return the parmTypeId
	 */
	public String getParmTypeId() {
		return parmTypeId;
	}

	/**
	 * @param parmTypeId
	 *            the parmTypeId to set
	 */
	public void setParmTypeId(String parmTypeId) {
		this.parmTypeId = parmTypeId;
	}

	/**
	 * @return the operator
	 */
	public String getOperator() {
		return operator;
	}

	/**
	 * @param operator
	 *            the operator to set
	 */
	public void setOperator(String operator) {
		this.operator = operator;
	}

	/**
	 * @return the size
	 */
	public int getSize() {
		return size;
	}

	/**
	 * @param size
	 *            the size to set
	 */
	public void setSize(int size) {
		this.size = size;
	}
}
