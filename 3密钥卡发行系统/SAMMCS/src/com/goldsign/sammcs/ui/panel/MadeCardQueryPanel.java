    /**
     * 查询
     * 
     */
    private void doQuery(){
        MakeCardQueryVo queryVo = getQueryVo();
        
        if (null == queryVo.getMakeCardTimeBegin() || "".equals(queryVo.getMakeCardTimeBegin()) 
                || null == queryVo.getMakeCardTimeEnd() || "".equals(queryVo.getMakeCardTimeEnd())) {
            MessageShowUtil.alertInfoMsg( "查询日期不能为空，查询日期段为三个月内！");
            return ;
        }
        if (queryVo.getMakeCardTimeBegin().compareTo(queryVo.getMakeCardTimeEnd())>0) {
            MessageShowUtil.alertInfoMsg( "查询开始日期不能大于结束日期！");
            return ;
        }

        Date beginOpTime = Util.fmtStrToDay(queryVo.getMakeCardTimeBegin());
        Date endOpTime = Util.fmtStrToDay(queryVo.getMakeCardTimeEnd());

        Calendar calendar = Calendar.getInstance();
        calendar.setTime(beginOpTime);
        calendar.add(Calendar.MONTH,3);
        Date after3MonthDay = calendar.getTime();
        if (after3MonthDay.before(endOpTime)) {
            MessageShowUtil.alertInfoMsg("请选择查询日期段为三个月内！");
            return ;
        }
        
        //查询
        List<Object[]> cards = makeCardService.queryIssueDetails(queryVo);
        
        if(cards.size()>0){
            btExport.setEnabled(true);//查询有数据时，导出按键可用
        }
        
        //生成列表
        genMainResultSetTable(cards);
        MessageShowUtil.infoOpMsg( "成功查询"+cards.size()+"条记录！");
    
    }
    
    /**
     * 根据结果集，生成列表
     * 
     * @param result 
     */
    private void genMainResultSetTable(List<Object[]> result) {
        //列名称
        String[] columnNames = AppConstant.COLUMN_NAMES;
        //列宽度
        int[] columnSize = AppConstant.COLUMN_SIZES;
        
        //生成列表
        resultSetTableMain = UIUtil.genResultSetTable(this.mainSPn, result, columnNames, columnSize);
    }

