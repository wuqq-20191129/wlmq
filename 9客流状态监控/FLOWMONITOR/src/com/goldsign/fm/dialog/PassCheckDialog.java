    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
          String pass = this.jPasswordFieldPass.getText().trim();

        if(!this.checkInput(pass))
             return;
         PassDao dao = new PassDao();
         PassResult ret ;
        try {
            ret =dao.checkPass(pass);
            if(ret.isIsSuccess())
            {
                this.dispose();
                this.checkResult.setIsSuccess(true);
                return;
            }
            else{
                PubUtil.prompt(this,ret.getMsg());
            }
        } catch (Exception ex) {
           
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

