    private boolean checkInput( String pass ){


        if(pass.length()==0){
            PubUtil.prompt(this,"密码不能为空");
            PubUtil.focus(jPasswordFieldPass);
            
            return false;
        }
        if(pass.length()!=6){
            PubUtil.prompt(this,"密码应为6位");
           PubUtil.focus(jPasswordFieldPass);
            return false;
        }
        
        return true;
    }
