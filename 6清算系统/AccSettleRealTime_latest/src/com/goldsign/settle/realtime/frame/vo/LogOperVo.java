package com.goldsign.settle.realtime.frame.vo;

/**
 *
 * @author hejj
 */
public class LogOperVo {

   private String waterNo;
   private String operObject;
   private String errLevel;
   private String remark;
   
   private String operatorId;
   private String genTime;

    /**
     * @return the waterNo
     */
    public String getWaterNo() {
        return waterNo;
    }

    /**
     * @param waterNo the waterNo to set
     */
    public void setWaterNo(String waterNo) {
        this.waterNo = waterNo;
    }

    /**
     * @return the operObject
     */
    public String getOperObject() {
        return operObject;
    }

    /**
     * @param operObject the operObject to set
     */
    public void setOperObject(String operObject) {
        this.operObject = operObject;
    }

    /**
     * @return the errLevel
     */
    public String getErrLevel() {
        return errLevel;
    }

    /**
     * @param errLevel the errLevel to set
     */
    public void setErrLevel(String errLevel) {
        this.errLevel = errLevel;
    }

    /**
     * @return the remark
     */
    public String getRemark() {
        return remark;
    }

    /**
     * @param remark the remark to set
     */
    public void setRemark(String remark) {
        this.remark = remark;
    }

    /**
     * @return the operatorId
     */
    public String getOperatorId() {
        return operatorId;
    }

    /**
     * @param operatorId the operatorId to set
     */
    public void setOperatorId(String operatorId) {
        this.operatorId = operatorId;
    }

    /**
     * @return the genTime
     */
    public String getGenTime() {
        return genTime;
    }

    /**
     * @param genTime the genTime to set
     */
    public void setGenTime(String genTime) {
        this.genTime = genTime;
    }
   
    
}
